//(SOIT107_Base_002) 基礎題：計程車資計算 :
//輸入里程公尺數，輸出應付的車資。計程車資計算方式為:
//起跳100元(1500公尺)，續跳5元(每250公尺)註:不足250公尺也要5元。

#include <stdio.h>
int main()
{
	int n;
	scanf("%d", &n);

	int ans = 0;
	if(n<=1500) ans = 100;
	else {
		ans = 100 + (n-1500)/250*5;
		if(n%250>0) ans += 5; //可惡,又多跳1次表
	}// n+249
	printf("%d", ans);
}
